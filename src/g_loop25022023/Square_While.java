package g_loop25022023;

public class Square_While {

    public static void main(String[] args) {
        int num =1; //Start with 1
        while (num<=10) //Until number 10
        {System.out.println(num*num*num); //Find Cube of num
        num++;} //num Increment


    }
}
/*Program will work from number 1 to number 10 (int - while)
Will print cube of num
Increment of num 1 = 2,   Further Increment of 2 = 3
1*1*1 = 1
2*2*2 = 8
3*3*3 = 27 and so on .... */